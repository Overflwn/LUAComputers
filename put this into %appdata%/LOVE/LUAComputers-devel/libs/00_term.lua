--One char: 6x6

_G.term = {}
local currentFont = "fonts/test.lf"

local file = filesystem.open(currentFont, "r")
local data = file:read()
--logError("font", pickle(c))
local chars = unpickle(tostring(data))
local bg, fg = {0,0,0}, {255,255,255}
local cX, cY = 1, 1
file:close()

function term.writeRawCharAtTotal(x, y, char)
	
	
	if chars[char] then
		for each, coordinate in ipairs(chars[char]) do
			gpu.drawPixel(x+coordinate[1], y+coordinate[2], fg)
		end
	end
end

function term.setBackgroundColor(newCol)
	if newCol and type(newCol) == "table" and #newCol == 3 then
		bg[1] = newCol[1]
		bg[2] = newCol[2]
		bg[3] = newCol[3]
	else
		return false, "invalid color table ({RRR,GGG,BBB})"
	end
end

function term.setForegroundColor(newCol)
	if newCol and type(newCol) == "table" and #newCol == 3 then
		fg[1] = newCol[1]
		fg[2] = newCol[2]
		fg[3] = newCol[3]
	else
		return false, "invalid color table ({RRR,GGG,BBB})"
	end
end

function term.getBackgroundColor() return bg end
function term.getForegroundColor() return fg end

function term.setCursorPos(x,y)
	--For term.writeText only
	if type(x) ~= "number" or type(y) ~= "number" then return false, "num, num expected" end
	if x > 40 then x = 40 end
	if y > 30 then y = 30 end
	if x < 1 then x = 1 end
	if y < 1 then y = 1 end
	cX = x
	cY = y
end

function term.getCursorPos()
	--For term.writeText only
	return cX, cY
end

function term.setFont(path)
	--WARNING: No real errorchecking, the whole thing will probably break if the font file is not serialized with pickle()
	if love.filesystem.isFile(path) then
		local file = filesystem.open(path, "r")
		local data = file:read()
		file:close()
		
		chars = unpickle(tostring(data))
	end
end

function term.writeRawCharAt(x, y, char)
	--chars have a 6x6 grid and the top left corner has the coordinate (0|0)
	--So the screen has a possibility of around 53x30 letters on the screen with this function
	--[[
		so the letter "A" basically looks like this:
		
		001100		  11
		010010		 1  1
		110011      11  11
		111111	--> 111111	
		110011	    11  11
		110011	    11  11
	]]
	
	if chars[char] then
		for each, coordinate in ipairs(chars[char]) do
			gpu.drawPixel(x*6-6+coordinate[1]+1, y*6-6+coordinate[2]+1, fg)
		end
	end
end

function term.writeText(text)
	
	local tmp_text = text
	local counter = 1
	
	--Just for drawRect
	local length = #text
	if length < 1 then length = 1 end
	gpu.drawRect(cX*8-8+1, cY*6-6+1, 8*length, 6, bg)
	repeat
		term.writeRawCharAtTotal( 8 * counter - 8 + cX*8 - 8 + 1, cY*6-6+1, string.sub(tmp_text, 1, 1))
		tmp_text = string.sub(tmp_text, 2)
		counter = counter+1
	until tmp_text == nil or #tmp_text == 0
	local oldx, oldy = term.getCursorPos()
	term.setCursorPos(oldx+#text, oldy)
end